<h3><i><b>Generalidades</b></i></h3>
<hr>
<b>Artículo 1.</b> Es responsabilidad de toda institución educativa contribuir a la formación integral de los
alumnos. Bajo este sentido, el presente reglamento general de alumnos tiene como objetivo fundamental
mantener la convivencia de la comunidad estudiantil en un ambiente de fraternidad y armonía, situación
propicia para lograr una formación técnica profesional basada en disciplina y responsabilidad de los
requerimientos que demanda la sociedad para alcanzar estándares de calidad.
La responsabilidad del cumplimiento del presente reglamento, está a cargo del personal directivo,
administrativo, docente, técnico, de apoyo e intendencia adscrito al Centro de Estudios Tecnológicos
industrial y de servicios No. 107; así mismo como para toda persona que se encuentre dentro del plantel.
<br><br>
<b>Artículo 2.</b> Este reglamento es de observancia obligatoria para todos los alumnos del Centro de Estudios
Tecnológicos industrial y de servicios No. 107, así como para toda persona que se encuentre dentro del
plantel.
<br><br>
<b>Artículo 3.</b> Toda violación a las disposiciones de este reglamento será motivo de una sanción establecido
en los artículos correspondientes al Título III. La Disciplina Escolar y Sanciones.
<br><br>
<b>Artículo 4.</b> En caso de reincidencia por parte de los alumnos, su caso se turnara al consejo técnico
consultivo.
<br><br>
<b>Artículo 5.</b> Para los efectos del presente reglamento, se entiende por DGETI a la Dirección General de
Educación Tecnológica Industrial y de Servicios.
<br><br>
<b>Artículo 6.</b> Para los efectos del presente reglamento se denomina Institución para referirnos al Centro de
Estudios Tecnológicos industrial y de servicios No. 107 (C.E.T.i.s. No. 107).
