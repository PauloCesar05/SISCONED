<h3><i><b>Capítulo I.- Control Escolar (Título II. Las Normas de Control Escolar)</b></i></h3>
<hr>
<b>Artículo 34.</b> Las materias son denominadas como:
<br>
<b>a) De asignatura:</b> Son las que se cursan en cada uno de los semestres y forman parte del “Componente
de Formación Básica” y “Componente de Formación Propedéutica”.
<br>
<b>b) De submódulo:</b> Son las propias de la carrera que se cursa y forman parte del “Componente de
Formación Profesional”.
<br>
<b>c) Módulo:</b> Es la agrupación de los Submódulos de la carrera.
<br><br>
<b>Artículo 35.</b> Es el alumno quien se encuentra inscrito en el sistema de Control Escolar de la Institución.
<br><br>
<b>Artículo 36.</b> Es alumno regular quien:
<br>
a) Acredite todas las asignaturas y módulos cursados.
<br>
b) Adeude hasta dos asignaturas o un módulo.
<br>
c) Adeude una asignatura y un submódulo.
<br><br>
<b>Artículo 37.</b> Es alumno irregular quien tenga más de 2 asignaturas reprobadas, o bien una asignatura y
más de un submódulo reprobados.
<br><br>
<b>Artículo 38.</b> Un alumno causa baja definitiva de la institución:
<br>
- Por solicitud propia del alumno, padre o tutor legal.
<br>
- Cuando al término de 5 años no haya logrado cumplir con la acreditación de todas sus asignaturas y
módulos.
<br>
- Como medida disciplinaria del plantel (artículos descritos en el Título III. La Disciplina Escolar y
Sanciones).
<br><br>
<b>Artículo 39.</b> El alumno cuenta con 5 años para acreditar la totalidad de asignaturas y módulos de la
carrera que cursa. Se dará de Baja Temporal en caso de adeudar 3 o más materias por semestre.
