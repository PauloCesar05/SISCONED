al semestre que esté inscrito, para tener derecho a ser evaluado.
<br><br>
<b>Artículo 25.</b> Asistir y participar en las actividades programadas por el Tutor en las dos horas de tutorías
establecidas en el horario de clases.
<br><br>
<b>Artículo 26.</b> Dirigirse con respeto, cortesía y urbanidad a las autoridades, personal docente,
administrativo, de apoyo y sus compañeros.
<br><br>
<b>Artículo 27.</b> Respetar y dar cumplimiento a los Acuerdos de Convivencia establecidos por el Comité
Escolar.
<br><br>
<b>Artículo 28.</b> Entregar en tiempo y forma el comprobante de la cuota de cooperación voluntaria a la oficina
de Contraloría, con la cual contribuye al mejoramiento y buen estado de las instalaciones y equipo de la
Institución.
<br><br>
<b>Artículo 29.</b> Informar de los cambios de domicilio, correo electrónico y/o teléfono que realice a la oficina
de Control Escolar.
<br><br>
<b>Artículo 30.</b> Portar en lugar visible su credencial de estudiante durante su estancia en la Institución la
cual es gratuita al inicio del ciclo escolar y solicitar el refrendo correspondiente con costo para el alumno.
<br><br>
<b>Artículo 31.</b> Participar en las actividades de simulacros y seguridad que promueva y determine la
Institución y la DGETI.
<br><br>
<b>Artículo 32.</b> Justificar al área de Orientación Educativa las inasistencias originadas por enfermedad,
