<h3><i><b>NORMAS DE CONVIVENCIA DE LOS ALUMNOS A PARTIR DEL VIRUS SARS-COV2</b></i></h3>
1.-Respetar los filtros de acceso instalados por el personal del plantel: como toma de temperatura y uso de
gel antibacterial.
<br>
2.- Portar siempre cubrebocas y de manera adecuada dentro del plantel y aula.
<br>
3.- Lavado continúo de manos y utilización de gel antibacterial.
<br>
