<h3><i><b>Capítulo IV.- Servicio Social y Prácticas Profesionales</b></i></h3>
<hr>
<b>Artículo 56.</b> Es obligatoria la realización del servicio social y las prácticas profesionales por parte del
alumno acorde a la carrera que cursa. Es responsabilidad del alumno acudir a la oficina de Servicio Social
y al Departamento de Vinculación con el Sector Productivo para realizar en tiempo y forma su registro y
trámite correspondiente para el inicio y término de los mismos.
<br><br>
<b>Fracción I.</b> Para la liberación del servicio social, el alumno deberá cubrir un total de 480 horas. El
prestador que sea alumno regular podrá solicitar autorización para realizar el servicio social a partir del
momento en que haya cubierto el 70% de créditos correspondientes al plan de estudios de la carrera o
especialidad que cursa.
<br><br>
<b>Fracción II.</b> Las prácticas profesionales son la estancia temporal de los alumnos en empresas o
instituciones del sector productivo de bienes y servicios, donde estos realizarán actividades acordes a su
perfil profesional. La duración de la práctica profesional será de 240 horas para el bachillerato bivalente.
Para la liberación de las prácticas profesionales, el alumno deberá realizarlas de acuerdo a su especialidad,
ya que no serán liberadas por ninguna actividad curricular a excepción a excepción de quienes ya se
encuentren laborando, demostrándolo con sus recibos de nómina en tiempo y forma.
