<h3><i><b>Capítulo II.- La Evaluación</b></i></h3>
<hr>
<b>Artículo 40.</b> El alumno que no cumplió con el 80% de asistencias durante el semestre, aunque muestre
calificación aprobatoria parcialmente se registrará como calificación NP (No Presentó) al final del semestre.
Por lo cual no tiene derecho a la primera evaluación extraordinaria y únicamente podrá acreditar la
asignatura en cursos intersemestrales o recursamiento semestral; si se trata de un módulo
necesariamente tiene que realizar el recursamiento semestral.
<br><br>
<b>Artículo 41.</b> Cuando el alumno no acredita un submódulo, el módulo correspondiente está reprobado.
Podrá regularizar el submódulo que adeuda en curso intersemestral antes de la reinscripción del siguiente
semestre, de lo contrario, el módulo estará reprobado y deberá recursar en periodo semestral todo el
módulo para acreditarlo.
<br><br>
<b>Artículo 42.</b> En el curso semestral se deberán presentar las tres evaluaciones parciales de acuerdo al
calendario escolar y en caso de no presentar alguna evaluación pasará de forma directa a extraordinario
o recursamiento. La escala de calificaciones parciales es de 0 a 10 expresada en números reales.
<br><br>
<b>Artículo 43.</b> La calificación final será el promedio de las tres calificaciones parciales, teniendo como
calificación mínima aprobatoria el seis (6).
<br><br>
<b>Artículo 44.</b> La escala de la calificación será de 5 a 10, expresada en números enteros, la cual se
registrará en acta única.
<br><br>
<b>Artículo 45.</b> La calificación de los módulos es el promedio de las calificaciones de los submódulos,
ponderados de acuerdo a las horas clase semanales. Esta calificación es la que aparece en el Historial
Académico y en el Certificado.
<br><br>
<b>Artículo 46.</b> Las asignaturas tienen el siguiente proceso de evaluación:
<br><br>
<b>Artículo 47.</b> Para que el alumno se regularice en las asignaturas tiene como primer oportunidad presentar
un examen extraordinario, en caso de no aprobarlo, tendrá que solicitar un recursamiento y así
sucesivamente mientras no exceda los 5 años para concluir su bachillerato.
<br><br>
<b>Artículo 48.</b> Los módulos/submódulos tienen el siguiente proceso de evaluación:
<br><br>
<b>Artículo 49.</b> Para que el alumno se regularice en los módulos y/o submúdulos tiene como única opción
solicitar recursamientos mientras no exceda los 5 años para concluir su bachillerato.
<br><br>
<h3><i><b>Capitulo III.- La Reinscripción</b></i></h3>
<hr>
<b>Artículo 50.</b> La asignación de carrera para el segundo semestre, se dará de acuerdo al orden de
preferencia de especialidad que el alumno escogerá en encuesta previa al periodo de reinscripción y al
promedio obtenido al final del primer semestre (antes de extraordinario).
<br><br>
<b>Artículo 51.</b> El alumno podrá reinscribirse al semestre inmediato superior cuando: cumpla lo especificado
en el artículo 36.
<br><br>
<b>Artículo 52.</b> El alumno irregular (artículo 37) no podrá realizar el proceso de reinscripción al semestre
inmediato superior y solo podrá reinscribirse a recursamientos y/o exámenes extraordinarios.
<br><br>
<b>Artículo 53.</b> Un alumno que adeude asignaturas, solamente podrá solicitar y presentar un máximo de
tres exámenes extraordinarios en cada periodo establecido (Enero y Agosto).
<br><br>
<b>Artículo 54.</b> Un alumno podrá solicitar inscripción a cursos intersemestrales de máximo dos asignaturas,
dos submódulos o la combinación de una asignatura y un submódulo.
<br><br>
<b>Artículo 55.</b> Un alumno irregular podrá solicitar inscripción a cursos semestrales máximo 6 asignaturas,
o 2 módulos, o en su caso 1 asignatura y 1 módulo.
