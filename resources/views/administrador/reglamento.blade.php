reglamento, comprometiéndose a su cabal observancia sin excepción, mediante su aceptación al
inscribirse.
<br><br>
<b>Artículo 22.</b> Participar activamente en las actividades académicas, deportivas y culturales establecidas
en el calendario escolar correspondiente. Será obligatorio inscribirse en el primer semestre en alguna de
estas actividades culturales y deportivas.
<br><br>
<b>Artículo 23.</b> Colaborar en todos los eventos académicos, deportivos y culturales que organice la
